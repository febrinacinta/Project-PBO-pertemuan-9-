class Burung:
    def intro(self):
        print("Ada beberapa burung.")
    
    def terbang (self):
        print("Kebanyakan burung bisa terbang, tapi ada burung yang tidak bisa terbang.")

    def tipe(self): 
        print("Beberapa burung merupakan omnivora, karnivora, herbivora")

    def habitat(self):
        print("Habitat setiap jenis burung berbeda-beda")

    def keaktifan(self):
        print("Ada beberapa burung yang aktif malam hari(nokturnal) dan aktif siang hari")

    def paruh(self):
        print("Setiap burung memiliki tipe paruh masing-masing")
    
    def kaki(self):
        print("Setiap burung memiliki tipe kaki yang berbeda")

